KString KKeyNames::GetKeyName(i32 key)
{
	if (KeyNames.contains(key))
		return KeyNames[key];

	return "";
}

i32 KKeyNames::GetKeyFromName(const KString& name)
{
	if (RevNames.contains(name))
		return RevNames[name];

	return -1;
}

KString KKeyNames::GetActionName(u8 bind)
{
	if (BindNames.contains(bind))
		return BindNames[bind];

	return "";
}

KString KKeyNames::GetActionName(EInputAction bind)
{
	return GetActionName(u8(bind));
}

u8 KKeyNames::GetActionFromName(const KString& name)
{
	KString n = name.ToLower();
	if (RevBinds.contains(n))
		return RevBinds[n];

	return u8(EInputAction::INVALID);
}

KString KKeyNames::CorrectActionCase(const KString& action)
{
	return GetActionName(GetActionFromName(action));
}

bool KKeyNames::KeyNameIsValid(const KString& k)
{
	return RevNames.contains(k);
}
